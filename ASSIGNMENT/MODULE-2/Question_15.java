/*
	W.A.J.P to concatenate a given string to the end of another string.
*/
 
package assignment_module_2;

public class Question_15 {
	
	public static void main(String[] args) {
		
		String firstName = "MIRAL";
		
		String lastName = "DUDHAGARA";
		
		System.out.println("First Name : "+firstName);
		
		System.out.println("Last Name : "+lastName);
		
		System.out.println("Concatinate a String to end of another String : "+lastName.concat(firstName));
		
	}

}