package operators_practice;

public class ArithmeticOperators {
	
	public static void main(String[] args) {
		
		int a = 1;
		int b = 11;
		
		//Addition
		int sum = a + b;
		System.out.println("Addition of a and b : "+sum);
		
		//Substraction
		int sub = b - a;
		System.out.println("Substraction of a and b : "+sub);
		
		//Multiplication
		int multi = a * b;
		System.out.println("Multiplication of a and b : "+multi);
		
		//Division
		int div = b / a;
		System.out.println("Division of a and b : "+div);
		
		//Modulo
		int mod = a % b;
		System.out.println("Modulo of a and b : "+mod);
	}
	
}