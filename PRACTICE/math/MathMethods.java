package math;

public class MathMethods {
	
	public static void main(String[] args) {
		
		/*		
			-> returns the positive value of a number
			
			System.out.println(Math.abs(-11.11));
		*/
		
		
		/*
			-> adds two integers
			-> prevents incorrect results that can occur from adding really large numbers
			
			System.out.println(Math.addExact(12345, 12345));
		*/
		
		
		/*
			-> Cube Root
		
			System.out.println(Math.cbrt(27));
		*/
		
		
		/*
			-> rounds a number UP to the nearest integer
				
			float num = 3.5F;
			System.out.println(Math.ceil(num));
		*/
		
		
		/*
			-> rounds a number DOWN to the nearest integer
			
			float num = 3.5F;
			System.out.println(Math.floor(num));
		 */
		
		
		/*
			-> returns the division between two integers rounded down
		
			System.out.println(Math.floorDiv(10, 5));
		*/
		
		
		/*
			-> returns the remainder of a division between two integers where the result of the division was rounded down
			
			System.out.println(Math.floorMod(10, 5));
		*/
		
		
		/*
			-> returns the value of the first number with the sign of the second number
			
			System.out.println(Math.copySign(4.5, -1));
			System.out.println(Math.copySign(-4.5, 1));
		*/
		
		
		/*
			-> returns 1 less than a specified integer
			
			System.out.println(Math.decrementExact(12345));
		*/
		
		
		/*
			-> multiplies two integers
			
			System.out.println(Math.multiplyExact(123, 123));
		*/
		
		
		/*
			-> subtraction two integers
		
			System.out.println(Math.subtractExact(123, 123));
		*/
		
		
		/*
			-> returns an integer with equal value and opposite sign
		
			System.out.println(Math.negateExact(123));
		*/
		
				
		/*
			-> returns 1 more than a specified integer
		
			System.out.println(Math.incrementExact(12345));
		*/
		
		
		/*
			-> Converts a long value to an int
		
			System.out.println(Math.toIntExact(12345L));
		*/
		
		
		/*
			-> returns the number with the highest value from a pair of numbers
			
			System.out.println(Math.max(11, 1));
		*/
		
		
		/*
			-> returns the number with the lowest value from a pair of numbers
		
			System.out.println(Math.min(11, 1));
		*/
		
	
		/*
			-> raises a number to the power of another number
			
			System.out.println(Math.pow(2, 3));
		*/
		
		
		/*
			-> returns a random number between 0 and 1
			
			System.out.println(Math.random(2, 3));
		 */
		
		
		/*
			-> rounds a number to the nearest integer
			
			System.out.println(Math.rint(0.5));
			System.out.println(Math.round(0.5));
		*/
		
		
		/*
			-> returns the result of the formula
			
			FORMULA:
			xÂ·2 root y
			
			System.out.println(Math.scalb(2, 2));
		*/
		
		
		/*
			->returns 1 for positive numbers, -1 for negative numbers and 0 for the number 0.
			
			System.out.println(Math.signum(0));
			System.out.println(Math.signum(8));
			System.out.println(Math.signum(-7));
		*/
		
		
		/*
			-> Square Root
			
			System.out.println(Math.sqrt(9));
		*/
		
	}

}