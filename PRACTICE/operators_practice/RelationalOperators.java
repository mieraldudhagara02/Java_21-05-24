package operators_practice;

public class RelationalOperators {
	
	public static void main(String[] args) {
		
		int a = 2;
		int b = 4;
		
		//Equals to
		System.out.println("a Equals To b : "+(a==b));
		
		//Not Equals To
		System.out.println("a is Not Equals To b : "+(a!=b));
		
		//GreaterThan
		System.out.println("b is GreaterThan a : "+(b>a));
		
		//LessThan
		System.out.println("a is LessThan b : "+(a<b));
		
		//GreaterThan or Equals To
		System.out.println("b is GreaterThan or Equals To a : "+(b>=a));
		
		//LessThan or Equals To
		System.out.println("a is LessThan or Equals To b : "+(a<=b));
		
	}

}